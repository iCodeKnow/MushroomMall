<template>
  <div class="tab-control">
    <div
      v-for="(item,index) in titles"
      :key="index"
      class="tab-control-item"
      :class="{active:index === currentIndenx}"
      @click="itemClick(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      }
    }
  },
  data() {
    return {
      currentIndenx: 0
    };
  },
  methods: {
    itemClick(index) {
      this.currentIndenx = index;
      this.$emit("itemClick", index);
    }
  }
};
</script>

